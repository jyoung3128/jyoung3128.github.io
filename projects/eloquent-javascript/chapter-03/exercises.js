// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(n) {
  if (n < 0){
    return false;
  } else if (n % 2 === 0 || n === 0){
    return true;
  }
  return false;
}

